import 'dart:math';
void main()
{
  int num1 = 10;
  int num2 = 2;

  num powernum = pow(num1,num2); // 1º o numero 2º potencia
  num maxnum = max(num1,num2); // mostra o maior
  num minnum = min(num1,num2); // mostra o menor 
  num squareroot = sqrt(25); // raiz quadrada de 25
  
  print("Potencia é $powernum"); 
  print("Maximo é $maxnum"); 
  print("Minimo é $minnum"); 
  print("raiz quadrada é $squareroot"); 
  
}