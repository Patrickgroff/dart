void main() {
// declarando dois números
 int num1=0;
 int num2=0;
 
// realizando operador de incremento/decremento

// pré incremento
num2 = ++num1;
print("O valor de num2 é $num2");

// redefine o valor para 0
num1 = 0;
num2 = 0;

// posta incremento
num2 = num1++;
print("O valor de num2 é $num2");
  
}