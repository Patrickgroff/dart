void main() {
  double idade = 24;
  idade+= 1; // Aqui idade+=1 significa idade = idade + 1.
  print("Após a adição a idade é $idade");
  idade-= 1; //Aqui idade-=1 significa idade = idade - 1.
  print("Depois da subtração a idade é $idade");
  idade*= 2; //Aqui idade*=2 significa idade = idade * 2.
  print("Após a multiplicação a idade é $idade");
  idade/= 2; //Aqui idade/=2 significa idade = idade / 2.
  print("Depois da divisão a idade é $idade");
}